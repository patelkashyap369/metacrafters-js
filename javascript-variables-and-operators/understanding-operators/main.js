// addition
var myVar = 5 + 10;
console.log(myVar);

// subtraction
myVar = 12 - 6;
console.log(myVar);

// multiplication
myVar = 13 * 13;
console.log(myVar);

// division
myVar = 16 / 2;
console.log(myVar);

// increment
var i = 1;
i++;
console.log(i);
i = 1;
i = i + 1;
console.log(i);

// decrement
i--;
console.log(i);
i = 2;
i = i - 1;
console.log(i);

// moduluo or remainder operator
console.log(5 % 2); // 1 - Remainder
console.log(Math.floor(5 / 2)); // 2 - Quotient
/*
    2 * 2 = 4
    5 - 4 = 1 (Remainder)
*/

// checking divisibility by 2
console.log(17 % 2) // 1 (17 is Odd)
console.log(48 % 2) // 0 (48 is Even)
